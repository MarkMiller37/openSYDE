//----------------------------------------------------------------------------------------------------------------------
/*! \brief   Redo delete
*/
//----------------------------------------------------------------------------------------------------------------------
void C_SyvDaUnoDeleteCommand::redo(void)
{
   this->m_DeleteSave();
   QUndoCommand::redo();
}

//----------------------------------------------------------------------------------------------------------------------
/*! \brief   Undo delete
*/
//----------------------------------------------------------------------------------------------------------------------
void C_SyvDaUnoDeleteCommand::undo(void)
{
   this->m_Restore();
   QUndoCommand::undo();
}
