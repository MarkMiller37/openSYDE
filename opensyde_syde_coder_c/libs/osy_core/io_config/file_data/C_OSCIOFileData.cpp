C_OSCIOFileData::C_OSCIOFileData(void)
{
}

//----------------------------------------------------------------------------------------------------------------------
/*! \brief  Get domain size

   \return
   Current domain count
*/
//----------------------------------------------------------------------------------------------------------------------
uint32 C_OSCIOFileData::GetDomainSize(void) const
{
   return this->c_Domains.size();
}

//----------------------------------------------------------------------------------------------------------------------
/*! \brief  Get domain

   \param[in] ou32_Index Index

   \return
   NULL Invalid index
   Else Valid domain
*/
//----------------------------------------------------------------------------------------------------------------------
const C_OSCIOFileDataDomain * C_OSCIOFileData::GetDomainFileDataConst(const uint32 ou32_Index) const
{
   const C_OSCIOFileDataDomain * pc_Retval = NULL;

   if (ou32_Index < this->c_Domains.size())
   {
      pc_Retval = &this->c_Domains[ou32_Index];
   }
   return pc_Retval;
}

//----------------------------------------------------------------------------------------------------------------------
/*! \brief  Add domain

   \param[in] orc_Domain Domain
*/
//----------------------------------------------------------------------------------------------------------------------
void C_OSCIOFileData::AddDomain(const C_OSCIOFileDataDomain & orc_Domain)
{
   this->c_Domains.push_back(orc_Domain);
}
